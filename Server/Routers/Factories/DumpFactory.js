function getObjectStructure(primaryContext, btnName) {
    var machedContextIndex = dumps.findIndex(x => x.contextName === primaryContext);
    if (Array.isArray(dumps[machedContextIndex].contextWiseData)) {
        // On The Fly
        var secondaryCtxData = dumps[machedContextIndex].contextWiseData;
        for (let i = 0; i < secondaryCtxData.length; i++) { // perfect 
            if (secondaryCtxData[i].name == btnName) {
                return secondaryCtxData[i].data;
            }
        }
    } else {
        // report
        return dumps[machedContextIndex].contextWiseData;
    }
}

